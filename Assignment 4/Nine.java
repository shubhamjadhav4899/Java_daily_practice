class Nine{
	 public static void main(String[] args) {
        System.out.println("First 10 natural numbers in reverse order");
        
        // Loop through the first 10 natural numbers in reverse order
        for(int i = 10; i > 0; i--) {
            System.out.print(i + " "); // Print the current number followed by a space
        }
    }

}