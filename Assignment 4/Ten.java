class Ten {
    public static void main(String[] args) {
        int sum = 0; // Initialize the sum to zero

        // Use a for loop to iterate through the first 50 natural numbers
        for (int i = 1; i <= 50; i++) {
            sum += i; // Add the current number (i) to the sum
        }

        // Display the result, which is the sum of the first 50 natural numbers
        System.out.println("The sum of the first 50 natural numbers is " + sum);
    }
}
